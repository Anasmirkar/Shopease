// Create a solid blue 32x32 ICO file
// Using a simpler approach: create directly as binary
const icoBuffer = Buffer.alloc(1174);
let offset = 0;

// ICO Header (6 bytes)
icoBuffer[offset++] = 0x00;  // Reserved
icoBuffer[offset++] = 0x00;  // Reserved
icoBuffer[offset++] = 0x01;  // Type (1 = ICO)
icoBuffer[offset++] = 0x00;  // Type
icoBuffer[offset++] = 0x01;  // Number of images
icoBuffer[offset++] = 0x00;  // Number of images

// Image Directory Entry (16 bytes)
icoBuffer[offset++] = 0x20;  // Width
icoBuffer[offset++] = 0x20;  // Height
icoBuffer[offset++] = 0x00;  // Colors
icoBuffer[offset++] = 0x00;  // Reserved
icoBuffer[offset++] = 0x01;  // Color planes
icoBuffer[offset++] = 0x00;  // Color planes
icoBuffer[offset++] = 0x18;  // Bits per pixel
icoBuffer[offset++] = 0x00;  // Bits per pixel
icoBuffer.writeUInt32LE(1074, offset); // Image data size
offset += 4;
icoBuffer.writeUInt32LE(22, offset); // Image data offset
offset += 4;

// Create a simple blue image (1024 bytes for 32x32x3 + 128 bytes for AND mask)
// Fill with blue color (RGB: 42, 82, 152)
for (let i = 22; i < 1078; i += 3) {
  icoBuffer[i] = 42;      // B
  icoBuffer[i + 1] = 82;  // G
  icoBuffer[i + 2] = 152; // R
}

fs.writeFileSync(path.join(assetsDir, 'icon.ico'), icoBuffer);
console.log('✅ Created icon.ico');

console.log(`✅ Icons ready at ${assetsDir}`);
